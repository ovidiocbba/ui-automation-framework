package com.ovidiomiranda.framework.core.driver;

/**
 * Enum for the Driver types.
 *
 * @author Ovidio Miranda
 */
public enum DriverType {
  /**
   * Google Chrome browser driver.
   */
  CHROME,
  /**
   * Google Chrome Headless browser driver.
   */
  CHROME_HEADLESS,
  /**
   * Microsoft Edge browser driver.
   */
  EDGE,
  /**
   * Microsoft Edge Headless browser driver.
   */
  EDGE_HEADLESS,
  /**
   * Mozilla Firefox browser driver.
   */
  FIREFOX,
  /**
   * FIREFOX Headless browser driver.
   */
  FIREFOX_HEADLESS
}
